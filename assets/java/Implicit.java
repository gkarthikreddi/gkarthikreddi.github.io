class Implicit {
    public static void main(String args[]) {
        byte b = 0;
        int i=251;
        double d=323.143;
        System.out.println("Conversion of byte to int:");
        i= (int)6;
        System.out.println("i: "+i+" b: "+b);
        System.out.println("Conversion of int to double:");
        d = (double)i;
        System.out.println("i: "+i+" d: "+b);
    }
}
